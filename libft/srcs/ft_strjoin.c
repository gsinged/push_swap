#include "libft.h"
#include <stdlib.h>

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	size_t	ln;
	size_t	i;

	if (!s1 || !s2)
		return (NULL);
	ln = ft_strlen(s1) + ft_strlen(s2);
	str = (char*)malloc(sizeof(*str) * (ln + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (*s1)
	{
		str[i] = *s1;
		s1++;
		i++;
	}
	while (*s2)
	{
		str[i] = *s2;
		s2++;
		i++;
	}
	str[i] = '\0';
	return (str);
}
